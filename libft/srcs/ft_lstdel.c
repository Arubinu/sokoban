#include "libft.h"

void	ft_lstdel(t_list **alst, void (*del)(void *, size_t))
{
	t_list	*temp;
	t_list	*next;

	if (*alst == NULL)
		return ;
	temp = *alst;
	while (temp)
	{
		if (del != NULL)
			del(temp->content, temp->content_size);
		next = temp->next;
		free(temp);
		temp = next;
	}
	*alst = NULL;
	return ;
}
